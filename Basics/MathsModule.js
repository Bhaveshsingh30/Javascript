// 1. Math.abs(x): Returns the absolute value of x.
let absoluteValue = Math.abs(-5);
console.log("Absolute Value:", absoluteValue);     //Absolute Value: 5

// 2. Math.ceil(x): Returns the smallest integer greater than or equal to x.
let ceilValue = Math.ceil(4.3);
console.log("Ceil Value:", ceilValue);            //Ceil Value: 5

// 3. Math.floor(x): Returns the largest integer less than or equal to x.
let floorValue = Math.floor(6.9);
console.log("Floor Value:", floorValue);         //Floor Value: 6

// 4. Math.round(x): Returns the value of x rounded to the nearest integer.
let roundedValue = Math.round(3.5);
console.log("Rounded Value:", roundedValue);    //Rounded Value: 4
 
// 5. Math.max(x, y, ...): Returns the largest of the provided values.
let maxNumber = Math.max(10, 5, 8);
console.log("Maximum Number:", maxNumber);     //Maximum Number: 10

// 6. Math.min(x, y, ...): Returns the smallest of the provided values.
let minNumber = Math.min(3, 7, 2);
console.log("Minimum Number:", minNumber);    //Minimum Number: 2

// 7. Math.pow(x, y): Returns x raised to the power of y.
let powerResult = Math.pow(2, 3);
console.log("Power Result:", powerResult);   //Power Result: 8

// 8. Math.sqrt(x): Returns the square root of x.
let squareRoot = Math.sqrt(25);
console.log("Square Root:", squareRoot);    //Square Root: 5

// 9. Math.random(): Returns a random floating-point number between 0 and 1.
let randomValue = Math.random();
console.log("Random Value:", randomValue);    //Random Value: 0.6911253251603267

// 10. Math.PI: A constant representing the mathematical constant pi.
let piValue = Math.PI;
console.log("PI Value:", piValue);          //PI Value: 3.141592653589793
